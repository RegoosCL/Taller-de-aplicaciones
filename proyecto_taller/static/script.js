var swiper = new Swiper('.mySwiper-1', {
    loop: true, // Habilita el bucle infinito
    autoplay: {
      delay: 3000, // Tiempo de pausa entre cada slide (en milisegundos)
      disableOnInteraction: false, // Permite que el autoplay continúe después de la interacción del usuario
    },
    pagination: {
      el: '.swiper-pagination',
      clickable: true,
    },
    navigation: {
      nextEl: '.swiper-button-next',
      prevEl: '.swiper-button-prev',
    },
});


let tabInputs = document.querySelectorAll(".tanInput");

tabInputs.forEach(function(input){

    input.addEventListener('change', function() {
        let id = input.ariaValueMax;
        let thisSwiper = document.getElementById('swiper' + id);
        thisSwiper.swiper.update();
    })

});